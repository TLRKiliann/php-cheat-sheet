<?php

// date
$myDate = date('Y-m-d');
echo $myDate;

echo "\n";

// date + time
$myDateTime = date('Y-m-d H:i:s');
echo $myDateTime;

echo "\n";

// first date of current month (-t)
$myFirstDate = date('Y-m-01');
echo $myFirstDate;

echo "\n";

// last date of current month
$myLastDate = date('Y-m-t');
echo $myLastDate;

echo "\n";

?>